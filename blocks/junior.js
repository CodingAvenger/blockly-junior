Blockly.Blocks['right'] = {
  init: function() {
    this.appendValueInput("NAME")
        .setCheck("JUNIOR")
        .appendField(new Blockly.FieldImage("https://raw.githubusercontent.com/oestape/blockly-jr-icons/master/png/jr_arrow_right.png", 32, 32, "right"));
    this.setOutput(true, "JUNIOR");
    this.setColour(180);
    this.setTooltip('');
    this.setHelpUrl('http://www.example.com/');
  }
};

